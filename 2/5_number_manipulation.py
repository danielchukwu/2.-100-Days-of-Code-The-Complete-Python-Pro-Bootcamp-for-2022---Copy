# round() function - rounds float to the amount of decimal numbers you want
print("round() function:")
print(round(23.55555, 2))       # => 23.56
print(round(2.1224567, 2))      # => 2.12
print(round(2.1311234, 4))      # => 2.1311

# floor division - division that divides and gives a result with no decimal numbers
print("floor division:")
print(8 // 3)                  # => 2
print(15 // 2)                 # => 7
print(19 // 6)                 # => 3

